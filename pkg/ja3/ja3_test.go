package ja3

import (
	"github.com/stretchr/testify/assert"
	"testing"
)

func TestJa3Digest(t *testing.T) {
	ja3 := Digest("")
	assert.Equal(t, "d41d8cd98f00b204e9800998ecf8427e", ja3)

	ja3 = Digest("771,4867-4866-4865-52393-52392-52394-49200-49196-49192-49188-49172-49162-159-107-57-65413-196-136-129-157-61-53-192-132-49199-49195-49191-49187-49171-49161-158-103-51-190-69-156-60-47-186-65-49169-49159-5-4-49170-49160-22-10-255,43-51-11-10-13-16,29-23-24-25,0")
	assert.Equal(t, "4f2655722e37c542ebeaf1eed48cbbbb", ja3)
}
